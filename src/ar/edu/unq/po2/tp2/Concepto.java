package ar.edu.unq.po2.tp2;
import ar.edu.unq.po2.tp2.*;


public class Concepto {

	private String nombre;
	private Float valor;
	
	public Concepto(String nombre, Float valor) {
		this.nombre = nombre;
		this.valor = valor;
	}
	
	public String getNombre() {
		return this.nombre;
	}
	
	public Float getValor() {
		return this.valor;
	}
	
	
}
